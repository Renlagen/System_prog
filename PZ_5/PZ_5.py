#Реализация хэш-таблиц. Вариант 1

Worker: dict = dict()
Worker[1] = 'Алексей'
Worker[2] = 'Добромиров'
Worker[3] = 3
Worker[4] = 45
Worker[5] = 'Каменщик'

key = int(input('Введите ключ от 1 до 5, где 1-Имя, 2-Фамилия, 3-Номер отдела, 4-Возраст, 5-Специальность\n'))

if 1 < key < 5:
    print(Worker[key])
else:
    print('Ошибка! Ключ Неопределен')



